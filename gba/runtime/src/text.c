static const uint8_t glyph_unknown[7] = {
    0x0e, 0x11, 0x01, 0x02, 0x04, 0x00, 0x04,
};

static const uint8_t *glyph_for(char ch) {
    if (ch >= '0' && ch <= '9') {
        return font_digits[ch - '0'];
    }
    if (ch >= 'a' && ch <= 'z') {
        return font_lower[ch - 'a'];
    }
    if (ch >= 'A' && ch <= 'Z') {
        return font_upper[ch - 'A'];
    }
    return glyph_unknown;
}

static uint8_t glyph_punctuation(char ch, unsigned row) {
    switch (ch) {
    case ' ':
        return 0;
    case '.':
        return row == 6 ? 0x04 : 0;
    case ':':
        return row == 2 || row == 5 ? 0x04 : 0;
    case '-':
        return row == 3 ? 0x1f : 0;
    case '+':
        return row == 1 || row == 2 || row == 4 || row == 5 ? 0x04 :
               row == 3 ? 0x1f : 0;
    case '/':
        return 0x01u << (6u - row < 5u ? 6u - row : 0u);
    case '_':
        return row == 6 ? 0x1f : 0;
    case '[':
        return row == 0 || row == 6 ? 0x0e : 0x08;
    case ']':
        return row == 0 || row == 6 ? 0x0e : 0x02;
    case '<':
        return row == 0 ? 0x02 :
               row == 1 ? 0x04 :
               row == 2 ? 0x08 :
               row == 3 ? 0x10 :
               row == 4 ? 0x08 :
               row == 5 ? 0x04 :
                          0x02;
    case '>':
        return row == 0 ? 0x08 :
               row == 1 ? 0x04 :
               row == 2 ? 0x02 :
               row == 3 ? 0x01 :
               row == 4 ? 0x02 :
               row == 5 ? 0x04 :
                          0x08;
    case '@':
        return row == 0 ? 0x0e :
               row == 1 ? 0x11 :
               row == 2 ? 0x17 :
               row == 3 ? 0x15 :
               row == 4 ? 0x17 :
               row == 5 ? 0x10 :
                          0x0e;
    case ',':
        return row == 5 ? 0x04 : row == 6 ? 0x08 : 0;
    case '?':
        return row == 0 ? 0x0e :
               row == 1 ? 0x11 :
               row == 2 ? 0x01 :
               row == 3 ? 0x02 :
               row == 4 ? 0x04 :
               row == 6 ? 0x04 : 0;
    case '!':
        return row <= 4 ? 0x04 : row == 6 ? 0x04 : 0;
    case '#':
        return row == 1 || row == 5 ? 0x0a :
               row == 2 || row == 4 ? 0x1f : 0x0a;
    case '%':
        return row == 0 ? 0x19 :
               row == 1 ? 0x1a :
               row == 2 ? 0x04 :
               row == 3 ? 0x08 :
               row == 4 ? 0x13 :
               row == 5 ? 0x03 : 0;
    case '^':
        return row == 0 ? 0x04 : row == 1 ? 0x0a : row == 2 ? 0x11 : 0;
    case '&':
        return row == 0 ? 0x0c :
               row == 1 ? 0x12 :
               row == 2 ? 0x14 :
               row == 3 ? 0x08 :
               row == 4 ? 0x15 :
               row == 5 ? 0x12 :
                          0x0d;
    case '*':
        return row == 1 ? 0x15 : row == 2 ? 0x0e :
               row == 3 ? 0x1f : row == 4 ? 0x0e :
               row == 5 ? 0x15 : 0;
    case '(':
        return row == 0 ? 0x02 :
               row == 1 ? 0x04 :
               row >= 2 && row <= 4 ? 0x08 :
               row == 5 ? 0x04 : 0x02;
    case ')':
        return row == 0 ? 0x08 :
               row == 1 ? 0x04 :
               row >= 2 && row <= 4 ? 0x02 :
               row == 5 ? 0x04 : 0x08;
    case ';':
        return row == 2 ? 0x04 : row == 5 ? 0x04 : row == 6 ? 0x08 : 0;
    case '$':
        return row == 0 ? 0x04 :
               row == 1 ? 0x0f :
               row == 2 ? 0x14 :
               row == 3 ? 0x0e :
               row == 4 ? 0x05 :
               row == 5 ? 0x1e :
                          0x04;
    default:
        return 0xff;
    }
}

void cl_gba_text_draw_char(int x, int y, char ch, uint8_t color) {
    uint8_t tile[32] = {0};
    for (unsigned row = 0; row < 7; ++row) {
        uint8_t bits = glyph_punctuation(ch, row);
        if (bits == 0xff) {
            bits = glyph_for(ch)[row];
        }
        for (unsigned col = 0; col < 5; ++col) {
            if (bits & (uint8_t)(0x10u >> col)) {
                unsigned offset = row * 4u + (col >> 1u);
                if (col & 1u) {
                    tile[offset] |= (uint8_t)((color & 0x0fu) << 4u);
                } else {
                    tile[offset] |= (uint8_t)(color & 0x0fu);
                }
            }
        }
    }
    uint16_t tile_id = cl_gba_video_alloc_text_tile();
    if (tile_id) {
        cl_gba_video_load_bg_tile(tile_id, tile);
        cl_gba_video_set_bg_tile(0, (uint8_t)(x >> 3), (uint8_t)(y >> 3),
                                 tile_id, 0);
        return;
    }
    for (unsigned row = 0; row < 7; ++row) {
        uint8_t bits = glyph_punctuation(ch, row);
        if (bits == 0xff) {
            bits = glyph_for(ch)[row];
        }
        for (unsigned col = 0; col < 5; ++col) {
            if (bits & (uint8_t)(0x10u >> col)) {
                cl_gba_video_put_pixel((uint16_t)(x + (int)col),
                                       (uint16_t)(y + (int)row), color);
            }
        }
    }
}

void cl_gba_text_draw(int x, int y, const char *text, uint8_t color) {
    if (!text) {
        return;
    }
    while (*text) {
        cl_gba_text_draw_char(x, y, *text++, color);
        x += 8;
    }
}

void cl_gba_text_draw_u32_hex(int x, int y, uint32_t value, uint8_t color) {
    char out[9];
    static const char hex[] = "0123456789ABCDEF";
    for (unsigned i = 0; i < 8; ++i) {
        out[i] = hex[(value >> ((7u - i) * 4u)) & 0x0fu];
    }
    out[8] = '\0';
    cl_gba_text_draw(x, y, out, color);
}
